package com.hospital.example;

public class Petient {
 private int roomNum;
 private String petientName;
 private String   disease;
 private int moNum;

public int getRoomNum() {
	return roomNum;
}
public void setRoomNum(int roomNum) {
	this.roomNum = roomNum;
}
public String getPetientName() {
	return petientName;
}
public void setPetientName(String petientName) {
	this.petientName = petientName;
}



public String getDisease() {
	return disease;
}
public void setDisease(String disease) {
	this.disease = disease;
}
public int getMoNum() {
	return moNum;
}
public void setMoNum(int moNum) {
	this.moNum = moNum;
}
	
	
}
