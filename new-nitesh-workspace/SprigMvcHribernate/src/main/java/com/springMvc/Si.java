package com.springMvc;

public class Si {

	private int p;
	private int r;
	private int t;
	
	
	public Si(int p, int r, int t) {
		super();
		this.p = p;
		this.r = r;
		this.t = t;
	}
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	public int getT() {
		return t;
	}
	public void setT(int t) {
		this.t = t;
	}
	@Override
	public String toString() {
		return "Si [p=" + p + ", r=" + r + ", t=" + t + "]";
	}
	
	
	
	
	
	
	
}
