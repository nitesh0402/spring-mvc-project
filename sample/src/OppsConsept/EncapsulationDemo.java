package OppsConsept;

public class EncapsulationDemo 
{
	private int id;
	private String name;
	
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setname(String name) 
	{
		this.name=name;
	}
	public  String getname() 
	{
		return name;
		
	}
	

	
}
